<p1><h4>VISION:</h4>
The vision of Bangladesh University is to increase and nurture talent with modern knowledge to meet the national needs in global perspective. To ensure a knowledge-based society and to compete the challenges of 21st century, it extends its focus on high standard quality education, innovation, discovery, research and creative activities. Thus, it will be able to generate capable and ethical leaders who will lead the nation and the globe as well. BU is committed to reflect the values and ambition of the nation.</p1><br>
<br>
<br>

